            var zvoleneMenu = "";
            $(document).ready(function () {

                setSpacer();
                
                

                hideAll();
                
                
                $(".slider").show();
                $(".slider").addClass("unrotated");

                $(".menuInfo").click(function () {
                    if (zvoleneMenu !== "info") {
                        zvoleneMenu = "info";
                        hideAll();

                        setTimeout(function () {
                            $(".info").show(400, function () {
                                $(".info").addClass("unrotated");
                            });
                        }, 600);
                    }
                });

                $(".menuKontakt").click(function () {

                    if (zvoleneMenu !== "kontakt") {
                        zvoleneMenu = "kontakt";
                        hideAll();

                        setTimeout(function () {
                            $(".kontakt").show(400, function () {
                                $(".kontakt").addClass("unrotated");
                            });
                        }, 600);
                    }

                });
                
                $(".menuCenik").click(function () {

                    if (zvoleneMenu != "cenik") {
                        zvoleneMenu = "cenik";
                        hideAll();

                        setTimeout(function () {
                            $(".cenik").show(400, function () {
                                $(".cenik").addClass("unrotated");
                            });
                        }, 600);
                    }

                });

                $(".fb").click(function () {

                });




                $(".prvniB").click(function () {
                    $('html, body').animate({
                        scrollTop: $(".prvni").offset().top - 10
                    }, 1000);
                });

                $(".spolupraceB").click(function () {
                    $('html, body').animate({
                        scrollTop: $(".spoluprace").offset().top - 10
                    }, 1000);
                });

                $(".workshopyB").click(function () {
                    $('html, body').animate({
                        scrollTop: $(".workshopy").offset().top - 10
                    }, 1000);
                });







                $(".socicon,.rotate").bind("webkitAnimationEnd mozAnimationEnd animationEnd", function () {
                    $(this).removeClass("otocka")
                })

                $(".socicon,.rotate").hover(function () {
                    $(this).addClass("otocka");
                }, function () {
                });



            });

            $(window).resize(function () {


                setSpacer();
            });

            function setSpacer() {
                $(".spacer").height($(window).height() / 100 * 13);
            }

            function hideAll() {
                $(".info").removeClass("unrotated");
                $(".kontakt").removeClass("unrotated");
                $(".cenik").removeClass("unrotated");

                $(".slider").removeClass("unrotated");

                setTimeout(function () {
                    $(".info").hide();
                    $(".kontakt").hide();
                    $(".cenik").hide();

                    $(".slider").hide();
                }, 500);

            }

