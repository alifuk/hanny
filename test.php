                        <h2 id="led1" style="font-size: small; color: #054f7f;">LED reflektory jsou vhodné pro vnitřní i venkovní použití</h2>
                        <p style="text-align: justify; text-justify: inter-word;">Většinu reflektorů můžete využít pro <strong>vnitřní i venkovní osvětlení</strong>. Poznáte to podle označení krytí, které je uvedené ve specifikacích u každého produktu. Reflektory <strong>pro venkovní aplikace</strong> jsou označené IP44 nebo IP65 a patří mezi ně všechny <strong>černé i stříbrné reflektory</strong> a také reflektory s PIR čidlem. Pouze <strong>pro vnitřní osvětlení</strong> jsou potom určené <strong>lištové reflektory</strong>, které mají krytí IP20. <strong>Možnosti</strong> jejich <strong>využití jsou velice široké</strong>. Využívají se k <strong>osvětlení hal, reklam, garáží, dílen, parkovišť, obchodů, zahrad, fasád, památek, cestiček, chodníčků i různých vjezdů</strong>. Pokud potřebujete osvítit místo, kde se pohybujete spíše krátce a nárazově, bude pro Vás tím pravým řešením <strong>reflektor s pohybovým PIR čidlem</strong>. Nebudete se tak muset zdržovat častým zhasínáním a rozsvícením, pohybový senzor to udělá za Vás. Velkou <strong>výhodou je okamžitý světelný výkon</strong>, <a title="Velký výběr svítidel na www.ledsviti.cz" href="/kategorie/LED-svitidla/">LED svítidla</a> se totiž nepotřebují zahřívat a jejich <strong>výkon nastupuje okamžitě</strong> v řádu milisekund. Navíc častým rozsvícením a zhasínáním nedochází k jejich poškozování, jsou proto jako stvořená k tomuto účelu.</p>
                        <h2 id="led10" style="font-size: small; color: #054f7f;">Lištové reflektory do interiéru</h2>
                        <p style="text-align: justify; text-justify: inter-word;"><strong>Lištové LED reflektory</strong> jsou ideální k použití v interiérových prostorech jako jsou <strong>obchody, galerie, showroomy či muzea</strong>. Lištový systém umožňuje rychle a jednoduše posunout světlo do jiné pozice a přizpůsobit tak rozvržení svítidel. Diodová světla jsou navíc díky svým vlasnostem ideální pro osvícení obrazů a jiných historických děl, protože nevydávají UV záření, které může z dlouhodobého hlediska způsobovat postupné blednutí barev a znehodnocování těchto děl.</p>
                        <h2 id="led11" style="font-size: small; color: #054f7f;">Přenosné reflektory se stojanem Vám budou vždy při ruce</h2>
